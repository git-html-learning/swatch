            -ms-filter: blur(8px);
            filter: blur(8px);"
    >
      <img src="@/assets/img/background.png" width="100%" height="100%" />
    </div>

    <div class="login-container">
      <el-form
        ref="loginForm"
        :model="loginForm"
        :rules="loginRules"
        class="login-form"
        auto-complete="on"
        label-position="left"
      >
        <div class="title-container">
          <h3 class="title">户外赛事智能安全保障平台</h3>
        </div>

        <el-form-item prop="username">
          <span class="svg-container">
            <svg-icon icon-class="user" />
          </span>
          <el-input
            ref="username"
            v-model="loginForm.username"
            placeholder="Username"
            name="username"
            type="text"
            tabindex="1"
            auto-complete="on"
          />
        </el-form-item>

        <el-form-item prop="password">
          <span class="svg-container">
            <svg-icon icon-class="password" />
          </span>
          <el-input
            :key="passwordType"
            ref="password"
            v-model="loginForm.password"
            :type="passwordType"
            placeholder="Password"
            name="password"
            tabindex="2"
            auto-complete="on"
            @keyup.enter.native="handleLogin"
          />
          <span class="show-pwd" @click="showPwd">
            <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
          </span>
        </el-form-item>

        <el-button
          :loading="loading"
          type="primary"
          style="width: 100%; margin-bottom: 30px"
          @click.native.prevent="handleLogin"
        >登录</el-button>
      </el-form>
    </div>
  </div>
</template>

<script>
// import { validUsername } from '@/utils/validate'
import { AdminLogin,UserLogin } from "@/api/admin";

export default {
  name: "Login",
  data() {
    const validateUsername = (rule, value, callback) => {
      if (!validUsername(value)) {
        callback(new Error("Please enter the correct user name"));
      } else {
        callback();
      }
    };
    const validatePassword = (rule, value, callback) => {
      if (value.length < 6) {
        callback(new Error("The password can not be less than 6 digits"));
      } else {
        callback();
      }
    };
    return {
      loginForm: {
        username: "admin",
        password: "123456"
      },

      // 通过 loginRules 属性传入约定的验证规则
      loginRules: {
        username: [
          { required: true, trigger: "blur", message: "请输入用户名" }
        ],
        password: [{ required: true, trigger: "blur", message: "请输入密码" }]
      },
      loading: false,
      passwordType: "password",
      redirect: undefined
    };
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect;
      },
      immediate: true
    }
  },
  methods: {
    showPwd() {
      if (this.passwordType === "password") {
        this.passwordType = "";
      } else {
        this.passwordType = "password";
      }
      this.$nextTick(() => {
        this.$refs.password.focus();
      });
    },
    // 在登录页面，点击登录按钮，调用handleLogin方法
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true;
          this.$store
            .dispatch("user/login", this.loginForm)
            .then(res => {
              if (res.code == 200) {
                console.log(res)
                   window.sessionStorage.setItem(
                  "token",
                  res.data.token
                );
                window.sessionStorage.setItem(
                  "username",
                  this.loginForm.username
                );
                this.$message({
                  showClose: true,
                  message: "登录成功",
                  type: "success"
                });
                          var user = {
                  username: "admin",
                  password: "123456"
                };
                console.log(user)
                UserLogin(user).then(res => {
                  console.log(res);
                  if (res.msg == "ok") {
                    var tokenForAdmin = res.data.token;
                    // console.log(tokenForAdmin)
                    window.sessionStorage.setItem(
                      "tokenForAdmin",
                      res.data.token
                    );
                  }
                });
                var adminData = {
                  username: "弘恩科技",
                  password: "hongenkj&ahu@2020"
                };
                AdminLogin(adminData).then(res => {
                  if (res.msg == "ok") {
                    console.log(res.data.token);
                    window.sessionStorage.setItem("adminToken", res.data.token);
                  }
                });
      
                this.$router.push({ path: "/homePage" });
                this.loading = false;
                this.$store.dispatch("user/login", this.loginForm);
              } else {
                this.$message({
                  showClose: true,
                  message: "提示:" + res.msg,
                  type: "error"
                });
                this.loading = false;
              }
            })
            .catch(() => {
              this.loading = false;
            });
        } else {
          console.log("error submit!!");
          return false;
        }
      });
    }
  }
};
</script>

